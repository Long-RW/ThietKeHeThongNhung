void Task_Humi(void)
{	
	printf("\n@%d: Task_Humi Enter!",uwTick); 

	float f_Humi;
	HAL_ADC_Start(&hadc1);
	u16_ADCVal = HAL_ADC_GetValue(&hadc1);
	HAL_Delay(50);
	
	f_Humi = (100.0 - (float)u16_ADCVal/4095*100);
	sprintf(c_Humi,"%2.2f%%",f_Humi);
	HAL_ADC_Stop(&hadc1);
	printf("\n@%d: Task_Humi Exit!\n",uwTick); 
	
}
